package com.bsit.model;

public class FileInfo {
	
	private String id;
	private String file_name;			//文件名称
	private String file_type;			//文件类型
	private String file_size;			//文件大小
	private String file_path;			//文件路径
	private String version_num;			//版本号
	private String version_name;		//版本名称
	private String version_desc;		//版本描述
	private String force_upgrade;		//是否强制升级	0：否	1：是
	private String upload_time;			//上传时间
	private String project_name;		//项目名称		P205	P207
	private String merchant_no;			//商户号
	private String remark;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFile_type() {
		return file_type;
	}
	public void setFile_type(String file_type) {
		this.file_type = file_type;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	public String getFile_size() {
		return file_size;
	}
	public void setFile_size(String file_size) {
		this.file_size = file_size;
	}
	public String getFile_path() {
		return file_path;
	}
	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}
	public String getVersion_num() {
		return version_num;
	}
	public void setVersion_num(String version_num) {
		this.version_num = version_num;
	}
	public String getVersion_name() {
		return version_name;
	}
	public void setVersion_name(String version_name) {
		this.version_name = version_name;
	}
	public String getVersion_desc() {
		return version_desc;
	}
	public void setVersion_desc(String version_desc) {
		this.version_desc = version_desc;
	}
	public String getForce_upgrade() {
		return force_upgrade;
	}
	public void setForce_upgrade(String force_upgrade) {
		this.force_upgrade = force_upgrade;
	}
	public String getUpload_time() {
		return upload_time;
	}
	public void setUpload_time(String upload_time) {
		this.upload_time = upload_time;
	}
	public String getProject_name() {
		return project_name;
	}
	public void setProject_name(String project_name) {
		this.project_name = project_name;
	}
	public String getMerchant_no() {
		return merchant_no;
	}
	public void setMerchant_no(String merchant_no) {
		this.merchant_no = merchant_no;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}

}
